def shell_sort(arr):
    mid = len(arr) // 2
    number_of_comparings = 0
    while mid > 0:
        i = 0
        j = mid
        while j < len(arr):
            if arr[i] >arr[j]:
                arr[i],arr[j] = arr[j],arr[i]
                number_of_comparings += 1
                print(str(number_of_comparings) + " - " + str(arr))

            i += 1
            j += 1
            k = i
            while k - mid > -1:
                if arr[k - mid] > arr[k]:
                    arr[k-mid],arr[k] = arr[k],arr[k-mid]
                    number_of_comparings += 1
                    print(str(number_of_comparings) + "- " + str(arr))
                k -= 1
        mid //= 2
    print("Number of Comparings is: " + str(number_of_comparings))
    return arr


